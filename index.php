	<div class="boxed">
		<p class="text2">The Academy Awards, popularly known as the Oscars,are awards for artistic and technical merit in the film industry. 
		They are regarded as the most famous and prestigious awards in the entertainment industry around the world
		. Given annually by the Academy of Motion Picture Arts and Sciences (AMPAS), the awards are an 
		international recognition of excellence in cinematic achievements, as assessed by the Academy's voting membership. The 
		various category winners are awarded a copy of a golden 
		statuette as a trophy, officially called the "Academy Award of Merit", although more commonly referred to 
		by its nickname, the "Oscar". The statuette depicts a knight rendered in the Art Deco style.</p>
		<p class="text2">The award was originally sculpted by George Stanley from a design sketch by Cedric Gibbons. 
		AMPAS first presented it in 1929 at a private dinner hosted by Douglas Fairbanks in the Hollywood Roosevelt Hotel in 
		what would become known as the 1st Academy Awards. The Academy Awards ceremony was first broadcast by radio in 1930 and was televised for 
		the first time in 1953. It is the oldest worldwide entertainment awards ceremony and is now televised live worldwide. It is 
		also the oldest of the four major annual American entertainment awards; its equivalents – the Emmy Awards for 
		television, the Tony Awards for theater, and the Grammy Awards for 
		music – are modeled after the Academy Awards. They are widely cited 
		as the most famous and prestigious competitive awards in the field of entertainment</p>
